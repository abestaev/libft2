#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*first;
	t_list	*new;
	void	*tmp;

	first = NULL;
	while (lst && f)
	{
		tmp = f(lst->content);
		new = ft_lstnew(tmp);
		if (!tmp || !new)
		{
			if (del)
				ft_lstclear(&first, del);
			return (NULL);
		}
		ft_lstadd_back(&first, new);
		lst = lst->next;
	}
	return (first);
}
